// cú pháp khởi tạo boolean: Boolean(<value>)
// chỉ ra các trường hợp mà khi dùng constructor Boolean trả về tru, false
console.log(Boolean(true));
console.log(Boolean(false));
console.log(Boolean(null));
console.log(Boolean(undefined));

// toán tử quan hệ là gì, các loại công dụng của từng biểu thức quan hệ
a = "5" === 5 && 4 == 4;
console.log(a);

b = "5" === 5 || 4 == 4;
console.log(b);
